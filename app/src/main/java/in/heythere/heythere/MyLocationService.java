    private void updateServerLocation(String lat,String lng,String uid) {

        //Server Background sync Here!!
        JSONParser jsonParser = new JSONParser();
        final String LOC_UPDATE_URL = "http://192.168.0.11/heyserver/server/location_update.php";



        List<NameValuePair> params = new ArrayList<>();


        params.add(new BasicNameValuePair("lat", lat));
        params.add(new BasicNameValuePair("lng", lng));
        params.add(new BasicNameValuePair("uid", uid));
        jsonParser.makeHttpRequest(LOC_UPDATE_URL, "POST", params);

    }

    private final Context mContext;

    // flag for GPS status
    boolean isGPSEnabled = false;

    // flag for network status
    boolean isNetworkEnabled = false;

